#1. pole trojkata o podstawie 10 i wysokosci 5
print("#1. pole trojkata o podstawie 10 i wysokosci 5:")
podstawa = 10
wysokosc = 5
pole_trojkata = (podstawa*wysokosc)/2
print(pole_trojkata,"\n")

#2. pole koła o promieniu 7
print("#2. pole koła o promieniu 7:")
promien_kola = 7
PI = 3.14
pole_kola = PI*(promien_kola**2)
print(pole_kola,"\n")
#3. pole trapezu o długości podstaw 3 i 9 oraz wysokości 6.5
print("#3. pole trapezu o długości podstaw 3 i 9 oraz wysokości 6.5:")
podstawa_a = 9
podstawa_b = 3
wysokosc = 6.5
pole_trapezu = (podstawa_a+podstawa_b*wysokosc)/2
print(pole_trapezu,"\n")

